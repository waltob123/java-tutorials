class DataTypes {
    public static void main(String args[]) {
        // Integer data types
        byte b = 10;
        short s = 20;
        int i = 30;
        long l = 40l;

        // Floating point data types
        float f = 50.0f;
        double d = 60.0;

        // Character data type
        char c = 'a';

        // Boolean data type
        boolean bool = true;
    }
}
